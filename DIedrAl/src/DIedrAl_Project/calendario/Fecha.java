package DIedrAl_Project.calendario;


public class Fecha {
    private int dia;

    private int mes;

    private int anyo;

    private int hora;

    int getDia() {
        return this.dia;
    }

    void setDia(int value) {
        this.dia = value;
    }

    int getMes() {
        return this.mes;
    }

    void setMes(int value) {
        this.mes = value;
    }

    int getAnyo() {
        return this.anyo;
    }

    void setAnyo(int value) {
        this.anyo = value;
    }

    int getHora() {
        return this.hora;
    }

    void setHora(int value) {
        this.hora = value;
    }

}
